def 두_수_더하기(첫번째_숫자, 두번째_숫자):
  """
  두 개의 숫자를 입력받아 더한 결과를 반환하는 함수입니다.

  Args:
    첫번째_숫자: 더할 첫 번째 숫자입니다.
    두번째_숫자: 더할 두 번째 숫자입니다.

  Returns:
    두 숫자를 더한 결과입니다.
  """
  결과 = 첫번째_숫자 + 두번째_숫자
  return 결과

